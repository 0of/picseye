class HammingDistancePolicy {
public:
  static constexpr const char Prefix = 'H';

public:
  static std::uint32_t distance(const std::string& s1, const std::string& s2) {
  	std::uint32_t d = 0;

  	auto len1 = s1.length();

  	auto d1 = s1.c_str();
  	auto d2 = s2.c_str();


  	for(auto i = 0; i != len1; ++i) {
    	d += popCountTable[d1[i] ^ d2[i]];
  	}

    return d;
  }
};

